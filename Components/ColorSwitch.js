export const colorSwitch = (color) => {
  switch (color) {
    case "White":
      return "#fff";
    case "Black":
      return "#000";
    case "Dark Grey":
      return "#666666";
    case "Light Grey":
      return "#B3B3B3";
    case "Beige Green":
      return "#6E7451";
    case "Washed-out Red":
      return "#BA6364";
    case "Navy Blue":
      return "#1A2B56";
  }
};
